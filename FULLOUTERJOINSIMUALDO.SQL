CREATE TABLE IF NOT EXISTS CLIENTE(
ID INT PRIMARY KEY auto_increment,
NOME VARCHAR(300)
);
CREATE TABLE IF NOT EXISTS PEDIDOS(
ID_PEDIDO INT PRIMARY KEY,
ID_CLIENTE INT,
PRODUTO VARCHAR(300)
);
