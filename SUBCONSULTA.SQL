CREATE TABLE IF NOT EXISTS PESSOA(
ID INT PRIMARY KEY auto_increment,
NOME VARCHAR(300)
);
CREATE TABLE IF NOT EXISTS IMOVEL(
ID INT PRIMARY KEY auto_increment,
IMOVEIS TEXT,
ID_IMOVEL INT,
foreign key(ID_IMOVEL)references PESSOA(ID)
);
