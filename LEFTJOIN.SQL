CREATE TABLE IF NOT EXISTS NOME(
ID INT PRIMARY KEY auto_increment,
NOME VARCHAR(100));

CREATE TABLE IF NOT EXISTS SOBRENOME(
ID INT primary KEY auto_increment,
NOME VARCHAR(100),
SOBRENOME INT,
foreign key(SOBRENOME)references NOME(ID)
);
